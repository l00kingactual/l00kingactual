# Constants
planck_length = 1.616e-35  # Planck length in meters
speed_of_light = 299792458  # Speed of light in meters per second

# Planck time for squared measure
planck_time_squared = (planck_length ** 2) / (speed_of_light ** 2)

# Planck time for cubed measure
planck_time_cubed = (planck_length ** 3) / (speed_of_light ** 3)

# Display the results
print("Planck time for squared measure:", planck_time_squared, "seconds")
print("Planck time for cubed measure:", planck_time_cubed, "seconds")
